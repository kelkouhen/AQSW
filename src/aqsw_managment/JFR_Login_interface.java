    private void jBLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBLoginActionPerformed
        
        // controler l'authentification de l'tlisateur avant de lancer la nouvelle fenetre.
        // creation de la requette
        String requetteSQL_SELECT_WHERE = "SELECT * from tuser WHERE user_login = '"+jTxtLogin.getText()+"';";
        
        
        try{
            stmt = AQSW_DB_CON.getCon().createStatement();
            resultRequete = stmt.executeQuery(requetteSQL_SELECT_WHERE);
            JOptionPane.showMessageDialog(null, "Requette SQL : Recherche si le login et mdp existe dans la BDD");
           
           
            while(resultRequete.next())
                {
                    System.out.println("pass1");
                    String userlogin = resultRequete.getString("user_login");
                    String userpwd = resultRequete.getString("user_password");
                    String userprofile = resultRequete.getString("user_profile");
                    
                    if(userlogin.equals(jTxtLogin.getText()))
                        {
                         System.out.println("pass2");
                         JOptionPane.showMessageDialog(null, "Login OK");
                         String c = String.valueOf(jPasswordFieldUser.getPassword());
                         if(userpwd.equals(c))
                            {
                                System.out.println("pass3");
                                jLuserProfile.setText(userprofile);
                                JOptionPane.showMessageDialog(null, "PWD OK");
                                JFR_SQE_MainFrame frmSQEMainFrame = new JFR_SQE_MainFrame();
                                frmSQEMainFrame.setVisible(true);
                            }
                         else
                            {
                                System.out.println("pass3");
                                JOptionPane.showMessageDialog(null, "PWD NOK");
                            }
                        }    
                    else
                        {
                           System.out.println("pass4");
                            JOptionPane.showMessageDialog(null, "User inconnu dans la BDD");
                        }
                        
                }            
            }
            
        catch(SQLException ex){
            System.out.println(ex);
            
        }
        
       
    }//GEN-LAST:event_jBLoginActionPerformed

    private void jBSubmitSubscriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSubmitSubscriptionActionPerformed
        
         // Creation d'un nouvel utilisateur
        String userloging = jTxtNewLogin.getText() ;
        char[] userpwd = jPasswordFieldNewUser.getPassword();
        
        String userprofile = jComboBoxUserProfile.getSelectedItem().toString();
        
        String requetteSQL_SUBMIT_NEW_USER = "INSERT INTO tuser(user_login,user_password,user_profile) VALUES('"+userloging+"','"+String.valueOf(userpwd)+"','"+userprofile+"')";

        try{
            stmt = AQSW_DB_CON.getCon().createStatement();
            stmt.executeUpdate(requetteSQL_SUBMIT_NEW_USER);
            JOptionPane.showMessageDialog(null, "Requette SQL ajout nouvel utilisateur dans la BDD executee avec succes");
        }
        catch(SQLException ex){
            System.out.println(ex);
        }
        
        
    }//GEN-LAST:event_jBSubmitSubscriptionActionPerformed

